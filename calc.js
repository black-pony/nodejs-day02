module.exports = {
    add:function(a,b){
        return parseFloat(a) + parseFloat(b);
    },
    sub:function(a,b){
        return parseFloat(a) - parseFloat(b);
    },
    mul:function(a,b){
        return parseFloat(a) * parseFloat(b);
    },
    div:function(a,b){
        return parseFloat(a) - parseFloat(b);
    }
};